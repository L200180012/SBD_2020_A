    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        if(txtIdpenump.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, ID Penumpang belum di isi!");
            txtIdpenump.requestFocus();
        }
        else if(txtnama.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Nama Penumpang belum di isi!");
            txtnama.requestFocus();
        }
        else if(txtjmlpenump.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, Jumlah Penumpang belum di isi!");
            txtjmlpenump.requestFocus();
        }
        else{
            try{
                String sql = "INSERT INTO penumpang VALUES ('"+txtIdpenump.getText()
                    +"','"+txtnama.getText()
                    +"','"+cbjenis.getSelectedItem()
                    +"','"+txtjmlpenump.getText()+"')";
                java.sql.Connection conn = (Connection)koneksi.configDB();
                java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
                pstm.execute();
                JOptionPane.showMessageDialog(null, "Proses Simpan Data Berhasil!");
                tampildata();
                kosongkanform();
            }catch(HeadlessException | SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_simpanActionPerformed

