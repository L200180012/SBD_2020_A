    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        if(txtidpemb.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Maaf, ID Pembelian belum di isi!");
            txtidpemb.requestFocus();
        }
        //else if(txtharga.getText().trim().equals("")){
          //  JOptionPane.showMessageDialog(null, "Maaf, Harga Tiket belum di isi!");
           // txtharga.requestFocus();
        //}
        else{
            try{
                String sql = "INSERT INTO pembelian VALUES ('"+txtidpemb.getText()
                    +"','"+cbkodeka.getSelectedItem()
                    +"','"+cbidpenump.getSelectedItem()
                    +"','"+txtharga.getText()
                    +"','"+txtjml.getText()
                    +"','"+txttotal.getText()+"')";
                java.sql.Connection conn = (Connection)koneksi.configDB();
                java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
                pstm.execute();
                JOptionPane.showMessageDialog(null, "Proses Simpan Data Berhasil!");
                tampildata();
                kosongkanform();
            }catch(HeadlessException | SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_simpanActionPerformed

