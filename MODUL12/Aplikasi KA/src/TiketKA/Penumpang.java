    private void tampildata(){
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("ID Penumpang");
        model.addColumn("Nama Penumpang");
        model.addColumn("Jenis Penumpang");
        model.addColumn("Jumlah Penumpang");
        
        try{
            int no = 1;
            String sql = "SELECT * FROM penumpang";
            java.sql.Connection conn = (Connection)koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{no++,
                    res.getString(1),
                    res.getString(2),
                    res.getString(3),
                    res.getString(4)}); 
            }   
            tabelpen.setModel(model); 
        }catch (SQLException e){
            System.out.println("Error : " + e.getMessage());
        }
    }
