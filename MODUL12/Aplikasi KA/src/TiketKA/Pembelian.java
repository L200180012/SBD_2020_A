    private void tabelpembMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelpembMouseClicked
        // TODO add your handling code here:
        int baris = tabelpemb.rowAtPoint(evt.getPoint());
        String idpm = tabelpemb.getValueAt(baris, 1).toString();
        txtidpemb.setText(idpm);
        String kodeka = tabelpemb.getValueAt(baris, 2).toString();
        cbkodeka.setSelectedItem(kodeka);
        String idpe = tabelpemb.getValueAt(baris, 3).toString();
        cbidpenump.setSelectedItem(idpe);
        String hrg = tabelpemb.getValueAt(baris, 4).toString();
        txtharga.setText(hrg);
        String jml = tabelpemb.getValueAt(baris, 5).toString();
        txtjml.setText(jml);
        String to = tabelpemb.getValueAt(baris, 6).toString();
        txttotal.setText(to);
    }//GEN-LAST:event_tabelpembMouseClicked

