    private void tabelpenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelpenMouseClicked
        // TODO add your handling code here:
        int baris = tabelpen.rowAtPoint(evt.getPoint());
        String idpenump = tabelpen.getValueAt(baris, 1).toString();
        txtIdpenump.setText(idpenump);
        String namapen = tabelpen.getValueAt(baris, 2).toString();
        txtnama.setText(namapen);
        String jenis = tabelpen.getValueAt(baris, 3).toString();
        cbjenis.setSelectedItem(jenis);
        String jml = tabelpen.getValueAt(baris, 4).toString();
        txtjmlpenump.setText(jml);
        
    }//GEN-LAST:event_tabelpenMouseClicked

